package course;

public class assigningOperators {

	public static void main(String[] args) {
		/*Assignment Operators*/
		
		int x = 100, y = 20;
		

		// operator and equal sign
		x += y;
		System.out.println(x);
		
		x -= y;
		System.out.println(x);
				
		x*= y;
		System.out.println(x);
		
		x /= y;
		System.out.println(x);
		
		x %= y;
		System.out.println(x);
		
		//plus plus method
		x++;
		System.out.println(x);
		x--;
		System.out.println(x);
		
		
	}

}
