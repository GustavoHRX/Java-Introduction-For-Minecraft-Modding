package course;

public class arithmeticAndNumbers {

	public static void main(String[] args) {
		//base numbers
		int x = 100, y = 20;
		
		//addition
		int result = x+y;
		System.out.println(result);
		
		//subtraction
		result = x-y;
		System.out.println(result);
		
		//multiplication
		result = x*y;
		System.out.println(result);
		
		//division
		result = x/y;
		System.out.println(result);
		
		//mod
		result = x%y;
		System.out.println(result);
		
		//math methods
		System.out.println(Math.max(x, y));
		System.out.println(Math.sqrt(x));
	}

}



